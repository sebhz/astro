local function get_location(location, country)
	local c = nil
	if country then c = locations[country] end
	if c == nil or country == nil then
		for i, v in pairs(locations) do
			for ii, vv in pairs(v) do
				if ii == location then
					local L, l = unpack(vv)
					return L, l, i
				end
			end
		end
	else
		for ii, vv in pairs(c) do
			if ii == location then
				local L, l = unpack(vv)
				return L, l, country
			end
		end
	end
	return nil
end

local function set_location(location, country)
	
	L, l, country = get_location(location, country)
	if L then
		astro.globals.latitude = L
		astro.globals.longitude = l
		return true
	end
	return false
end

if astro == nil then astro = {} end
astro["locations"] = {get_location = get_location,
                      set_location = set_location}
return(astro)
